    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Pokemon p = new Pokemon();
        p.setName(txtName.getText());
        p.setLevel(Integer.parseInt(txtLevel.getText()));
        p.setHp(Integer.parseInt(txtHP.getText()));
        p.setAgi(Integer.parseInt(txtAGY.getText()));
        p.setAtk(Integer.parseInt(txtATK.getText()));
        p.setDef(Integer.parseInt(txtDEF.getText()));
        p.setSpecialAtk(Integer.parseInt(txtSA.getText()));
        p.setSpecialDef(Integer.parseInt(txtSD.getText()));
        try{
            MySqlDAO m = new MySqlDAO();
            m.insert(p.getName(), p.getLevel(), p.getHp(), p.getAgi(), p.getAtk(), p.getDef(), p.getSpecialAtk(), p.getSpecialDef());
            JOptionPane.showMessageDialog(null, "Inserido com sucesso");
            update_table();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Falha ao inserir");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

