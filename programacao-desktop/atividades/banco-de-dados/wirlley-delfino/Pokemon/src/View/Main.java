    private void jTabMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabMouseClicked
        try{
            MySqlDAO m = new MySqlDAO();
            int row = jTab.getSelectedRow();
            String tableclick = (jTab.getModel().getValueAt(row, 0).toString());
            ResultSet rs = m.select_especifique(tableclick);
            if(rs.next()){
                String id = rs.getString("id");
                txtID.setText(id);
                String name = rs.getString("name");
                txtName.setText(name);
                String level = rs.getString("level");
                txtLevel.setText(level);
                String hp = rs.getString("hp");
                txtHP.setText(hp);
                String agi = rs.getString("agi");
                txtAGY.setText(agi);
                String atk = rs.getString("atk");
                txtATK.setText(atk);
                String def = rs.getString("def");
                txtDEF.setText(def);
                String specialAtk = rs.getString("specialAtk");
                txtSA.setText(specialAtk);
                String specialDef = rs.getString("specialDef");
                txtSD.setText(specialDef);
            }
        }catch(Exception e ){};
    }//GEN-LAST:event_jTabMouseClicked

