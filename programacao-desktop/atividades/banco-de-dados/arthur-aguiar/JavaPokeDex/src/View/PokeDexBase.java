    private void ScanBttActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ScanBttActionPerformed
        boolean erro = false;
        Pokemon poke = null;
        String nome = Txtnome.getText();
        if(nome == null || nome.isEmpty()){
            JOptionPane.showMessageDialog(null, "Nome necessario");
            erro = true;
        }
        int level = Integer.parseInt(Txtlevel.getText());
        if(level < 0){
            JOptionPane.showMessageDialog(null, "Nivel necessario ou não negativo seu lixo!!");
            erro = true;
        }
        int hp = Integer.parseInt(Txthp.getText());
        int agi = Integer.parseInt(Txtagi.getText());
        int atk = Integer.parseInt(Txtatk.getText());
        int def = Integer.parseInt(Txtdef.getText());
        int spatk = Integer.parseInt(Txtspa.getText());
        int spdef = Integer.parseInt(Txtspd.getText());
        if(!erro)
            poke = new Pokemon(nome,level,hp,agi,atk,def,spatk,spdef);
        
        dex.Insere(poke);
    }//GEN-LAST:event_ScanBttActionPerformed

