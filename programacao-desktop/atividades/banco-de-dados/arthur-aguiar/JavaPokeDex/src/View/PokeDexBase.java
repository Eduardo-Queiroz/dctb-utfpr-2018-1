    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        DefaultTableModel dbDex =(DefaultTableModel) Poketable.getModel();
        dbDex.getDataVector().removeAllElements();
        ArrayList<Pokemon> lista = dex.lista();
        for( Pokemon poke : lista ){
            Object[] conteudo = {poke.getName(),poke.getLevel()};
            dbDex.addRow(conteudo);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

